import ipeadatapy as ip

df_ipca = ip.timeseries("PRECOS_IPCAG")
df_ipca = df_ipca.loc["2010":"2024"]

df_ipca = df_ipca.rename(columns={"YEAR": "Ano", "VALUE ((% a.a.))": "IPCA"})

